const logger = getLogger('SubAgentPromptBuilder');

type IsolatedRuntimeSandboxMode = 'OfflineIsolated' | 'ControlledNetwork';

function isIsolatedRuntimeSandboxMode(
    sandboxMode: TaskContext['sandboxMode']
): sandboxMode is IsolatedRuntimeSandboxMode {
    return sandboxMode === 'OfflineIsolated' || sandboxMode === 'ControlledNetwork';
}

